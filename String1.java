class Strings{
    public static void main(String[] args){
        String name="Alice";//A-0, l-1, i-2,....
        System.out.println(name.charAt(1));
        System.out.println(name.charAt(4));
        System.out.println(name.length());//gives length of strings
    }
}
// output:
// l
// e
//5